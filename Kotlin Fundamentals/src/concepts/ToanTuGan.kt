package concepts

fun main(args: Array<String>){
    var x: Int = 8

    // Gán +=
    x += 5
    println("Sau khi gán +=: $x")

    // Gán -=
    x -= 3
    println("Sau khi gán -=: $x")

    // Gán *=
    x *= 2
    println("Sau khi gán *=: $x")

    // Gán /=
    x /= 4
    println("Sau khi gán /=: $x")

    // Gán %=
    x %= 3
    println("Sau khi gán %=: $x")
    
}